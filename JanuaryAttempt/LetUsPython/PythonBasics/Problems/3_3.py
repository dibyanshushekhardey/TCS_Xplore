# demonstrate hpw to convert from one numer type to another

# convert to int
print(int(3.14))
a=int('485')
b = int('768')
c=a+b
print(c)
print(int('1011', 2))
print(int('341', 8))
print(int('21', 16))

# convert to float
print(float(35))
i=float('4.85')
j=float('7.68')
k=i+j
print(k)

# convert to complex
print(complex(35))
x=complex(4.85, 1.1)
y=complex(7.68, 2.1)
z=x+y
print(z)

# convert to bool
print(bool(35))
print(bool(1.2))
print(int(True))
print(int(False))
